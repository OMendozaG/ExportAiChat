/*
 * Valores por defecto centralizados.
 * Se usan tanto en options como en content para garantizar comportamiento consistente.
 */
(() => {
  const root = globalThis.ChatExportAi;
  const { TEXT_FORMATTING, QUOTE_DIVIDER_STYLE, MEDIA_HANDLING, AI_NAME_MODE, MULTILINE_FORMAT } = root.constants;
  const { MAX_EXPORT_TIMEOUT_SECONDS } = root.constants;

  root.defaults = {
    settings: {
      // Nombre visible en el export para mensajes del usuario.
      humanName: "Human",

      // Nombre visible para IA: por nombre del proveedor o personalizado.
      aiNameMode: AI_NAME_MODE.PROVIDER,
      aiCustomName: "AI",

      // Formato de texto: limpiar formato o mantenerlo en Markdown.
      textFormatting: TEXT_FORMATTING.MARKDOWN,

      // Estilo común para blockquotes y líneas de separación.
      quoteDividerStyle: QUOTE_DIVIDER_STYLE.MARKDOWN,

      // Continuation style for multiline TXT exports.
      multilineFormat: MULTILINE_FORMAT.TAB,

      // Per-role TXT message headers, supports <HumanName>/<AiName>.
      txtHumanMessageHeaderTemplate: "\n\n----- <HumanName>:\n\n",
      txtAiMessageHeaderTemplate: "\n\n----- <AiName>:\n\n",

      // Separator inserted between TXT message blocks.
      messageSeparator: "",

      // Role border colors for HTML/PDF exports.
      htmlPdfAiBorderColor: "#2563eb",
      htmlPdfHumanBorderColor: "#f59e0b",

      // Automatic file naming and default naming template.
      autoFileName: true,
      fileNameTemplate: "<ChatNameCount*3>. <ChatName>",
      invalidFileNameReplacement: ".",

      // Download mode: save to Downloads, ask location, or save to custom Downloads subfolder.
      saveMode: "autosave",
      customDownloadFolder: "Chat Export AI",
      autosaveConflictAction: "overwrite",

      // App chrome theme: auto follows the browser/system preference.
      appTheme: "auto",

      // Shared metadata toggles for TXT, HTML, MHT and PDF exports.
      includeExportTitle: true,
      metadataEnabled: true,
      metadataExportedAt: true,
      metadataDeviceUser: false,
      metadataFolder: false,
      metadataTitle: false,
      metadataModel: true,
      metadataUrl: false,
      metadataSummaryProvider: false,
      metadataSummaryChatName: true,
      metadataSummaryMessages: true,
      metadataStartTime: false,
      metadataEndTime: false,
      metadataDuration: false,

      // Include visible reasoning/thinking blocks when the provider exposes them.
      includeThinking: true,

      // Include the visible thinking duration when the provider exposes it.
      includeThinkingDuration: true,

      // Include the message time when the provider can extract it reliably.
      includeMessageTime: false,

      // Include message ids starting at 1 in TXT, HTML and PDF exports.
      includeMessageId: false,

      // Include user attachment file names in exports when the provider exposes them.
      showUserAttachmentNames: true,

      // Include assistant references to user attachments in exports when exposed.
      showAssistantUserAttachmentReferences: true,

      // Include assistant file attachments generated by the AI in exports.
      showAssistantGeneratedAttachmentReferences: true,

      // Include assistant web links in exported synthetic reference lines.
      showAssistantWebReferences: false,

      // Show the inline "Export To..." button in supported chat headers.
      showHeaderExportButton: true,
      showHeaderExportButtonChatgpt: true,
      showHeaderExportButtonClaude: true,
      showHeaderExportButtonGemini: true,
      showHeaderExportButtonDeepseek: true,
      showHeaderExportButtonGrok: true,

      // Per-export timeout in seconds. Clamped to MAX_EXPORT_TIMEOUT_SECONDS.
      exportTimeoutSeconds: MAX_EXPORT_TIMEOUT_SECONDS,

      // Visible export format buttons across popup and inline export menu.
      showExportPdf: true,
      showExportMht: true,
      showExportHtml: true,
      showExportTxt: true,

      // Gestión global de media/no texto.
      mediaHandling: MEDIA_HANDLING.PLACEHOLDER,

      // Si true y hay media con modo MHT, al exportar TXT/HTML genera MHT adicional.
      companionMhtOnMedia: true
    }
  };

  function normalizeHexColor(value, fallback) {
    const raw = String(value || "").trim();
    if (/^#[0-9a-f]{3}$/i.test(raw) || /^#[0-9a-f]{6}$/i.test(raw)) {
      return raw.toLowerCase();
    }

    return fallback;
  }

  function normalizeSaveMode(value) {
    const candidate = String(value || "").trim().toLowerCase();
    if (candidate === "ask" || candidate === "custom" || candidate === "autosave") {
      return candidate;
    }

    return "autosave";
  }

  function normalizeAutosaveConflictAction(value) {
    return String(value || "").trim().toLowerCase() === "uniquify" ? "uniquify" : "overwrite";
  }

  function normalizeCustomDownloadFolder(value, fallback) {
    const rawValue = String(value ?? fallback ?? "").trim();
    const normalizedValue = rawValue.normalize ? rawValue.normalize("NFC") : rawValue;
    const sanitizedValue = normalizedValue
      .replace(/\\/g, "/")
      .replace(/[<>:"|?*\u0000-\u001F]/g, " ")
      .replace(/\s+/g, " ")
      .trim()
      .replace(/^[/.\s]+|[/.\s]+$/g, "");

    return sanitizedValue || fallback || "Chat Export AI";
  }

  // Función de merge defensivo: rellena faltantes sin perder settings existentes.
  root.defaults.mergeSettings = (incoming) => {
    const next = { ...root.defaults.settings };

    if (!incoming || typeof incoming !== "object") {
      return next;
    }

    for (const [key, value] of Object.entries(incoming)) {
      if (value !== undefined) {
        next[key] = value;
      }
    }

    // Backward compatibility: old global toggle seeds provider-specific toggles
    // if they are not present in storage yet.
    if (
      incoming.showHeaderExportButtonChatgpt === undefined &&
      incoming.showHeaderExportButton !== undefined
    ) {
      next.showHeaderExportButtonChatgpt = Boolean(incoming.showHeaderExportButton);
    }

    if (
      incoming.showHeaderExportButtonClaude === undefined &&
      incoming.showHeaderExportButton !== undefined
    ) {
      next.showHeaderExportButtonClaude = Boolean(incoming.showHeaderExportButton);
    }

    if (
      incoming.showHeaderExportButtonGemini === undefined &&
      incoming.showHeaderExportButton !== undefined
    ) {
      next.showHeaderExportButtonGemini = Boolean(incoming.showHeaderExportButton);
    }

    if (
      incoming.showHeaderExportButtonDeepseek === undefined &&
      incoming.showHeaderExportButton !== undefined
    ) {
      next.showHeaderExportButtonDeepseek = Boolean(incoming.showHeaderExportButton);
    }

    if (
      incoming.showHeaderExportButtonGrok === undefined &&
      incoming.showHeaderExportButton !== undefined
    ) {
      next.showHeaderExportButtonGrok = Boolean(incoming.showHeaderExportButton);
    }

    if (incoming.metadataConversationSummary !== undefined) {
      if (incoming.metadataSummaryProvider === undefined) {
        next.metadataSummaryProvider = Boolean(incoming.metadataConversationSummary);
      }

      if (incoming.metadataSummaryChatName === undefined) {
        next.metadataSummaryChatName = Boolean(incoming.metadataConversationSummary);
      }

      if (incoming.metadataSummaryMessages === undefined) {
        next.metadataSummaryMessages = Boolean(incoming.metadataConversationSummary);
      }
    }

    // Backward compatibility for the legacy single assistant references toggle.
    if (incoming.showAssistantReferences !== undefined) {
      if (incoming.showAssistantUserAttachmentReferences === undefined) {
        next.showAssistantUserAttachmentReferences = Boolean(incoming.showAssistantReferences);
      }

      if (incoming.showAssistantGeneratedAttachmentReferences === undefined) {
        next.showAssistantGeneratedAttachmentReferences = Boolean(incoming.showAssistantReferences);
      }

      if (incoming.showAssistantWebReferences === undefined) {
        // Keep web links disabled by default even for legacy migrations.
        // Legacy toggle controlled mixed reference kinds, but URL visibility
        // is now explicit and defaults to off.
        next.showAssistantWebReferences = false;
      }
    }

    const numericTimeout = Number(next.exportTimeoutSeconds);
    const safeTimeout = Number.isFinite(numericTimeout) ? numericTimeout : MAX_EXPORT_TIMEOUT_SECONDS;
    next.exportTimeoutSeconds = Math.min(
      MAX_EXPORT_TIMEOUT_SECONDS,
      Math.max(1, Math.round(safeTimeout))
    );
    next.saveMode = normalizeSaveMode(next.saveMode);
    next.autosaveConflictAction = normalizeAutosaveConflictAction(next.autosaveConflictAction);
    next.customDownloadFolder = normalizeCustomDownloadFolder(
      next.customDownloadFolder,
      root.defaults.settings.customDownloadFolder
    );
    next.htmlPdfAiBorderColor = normalizeHexColor(
      next.htmlPdfAiBorderColor,
      root.defaults.settings.htmlPdfAiBorderColor
    );
    next.htmlPdfHumanBorderColor = normalizeHexColor(
      next.htmlPdfHumanBorderColor,
      root.defaults.settings.htmlPdfHumanBorderColor
    );

    return next;
  };
})();
